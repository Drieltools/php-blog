        <h2>O que é Marketing Digital</h2>
        <div>
        <img width="450" height="257"
            src="./imagens/mkt-digital.png" />
        </div>
        <p>Marketing Digital é a promoção de produtos ou marcas por meio de mídias digitais. Ele é uma das principais
            maneiras que as empresas têm para se comunicar com o público de forma direta, personalizada e no momento
            certo.</p>
        <p>Philip Kotler — considerado o pai do Marketing — conceitua
            marketing como&nbsp;<em>“o processo social por meio do qual pessoas e grupos de pessoas satisfazem desejos e
                necessidades com a criação, oferta e livre negociação de produtos e serviços de valor com outros”.</em>
        </p>
        <p>Porém, as maneiras de fazer com que isso aconteça sofrem mudanças quase que diariamente. Por isso, atualmente
            é impossível falar de marketing e ignorar que a maior parte da população mundial está conectada à internet e
            faz compras online.</p>
        <p>É por isso que o Marketing Digital é a principal forma de fazer e conceituar marketing nos dias de hoje. O
            termo é usado para&nbsp;<strong>resumir todos os seus esforços de marketing no ambiente online</strong>.</p>
        <p>Usando canais digitais — como blogs, sites, motores de busca, mídias sociais, email e outros —, as empresas
            tentam solucionar dores e desejos de clientes e potenciais clientes.</p>
        <p>Você entendeu a “sacada” do conceito apresentado pelo professor Kotler? Trata-se de um processo social! É
            algo que depende do acordo entre duas pessoas dentro de um contexto.</p>
        <p>Marketing Digital pode ser realizado por pessoas, empresas, universidades, ONGs, associações, igrejas… Assim,
            também há fatores culturais, psicológicos, geográficos, jurídicos que influenciam as pessoas na hora de
            comprar ou vender um produto ou serviço.</p>
        <p><strong>E cada um deve fazer o que pode, com os meios que tem, para encontrar as oportunidades certas nesse
                meio</strong>!</p>
